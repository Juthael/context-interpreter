package com.tregouet.context_interpreter.io.outputs.exceptions;

public class VisualizationException extends Exception {
	
	private static final long serialVersionUID = -8166482691706194576L;

	public VisualizationException() {
		super();
	}

	public VisualizationException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

	public VisualizationException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public VisualizationException(String arg0) {
		super(arg0);
	}

	public VisualizationException(Throwable arg0) {
		super(arg0);
	}



}
